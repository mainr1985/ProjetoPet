    private void loadComboVeterinarios(){
        
        ControleFuncionario veterinario = new ControleFuncionario();
        List<Veterinario> lista = veterinario.listarVeterinarios();        
        if (!lista.isEmpty()){
            for (Veterinario v:lista){
                cmbResponsavel.addItem(v);    
            }
        }
    }    
    
